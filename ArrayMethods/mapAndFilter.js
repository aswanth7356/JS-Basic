
var a=[1,2,3,4,5]

// map()  -->  create an array with equal number of elemnts to the input array elements            ( ella elements edkan)
// eg :square
console.log(a.map(i=>i**2));                                                                                //(here i means elements of var a)





// filter()  --->   create an array with elements only for some elements in input array          (kurach elements edkan)
// eg : odd
console.log(a.filter(i=>i%2!=0));